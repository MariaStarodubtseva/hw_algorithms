#Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
#Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.

a = 5
b = 6
print (f'a = {a}')
print (f'b = {b}')

c = a & b
d = a | b
e = a ^ b
f = ~a
g = a >> 2
h = a << 2

print (f'a & b = {c}')
print (f'a | b = {d}')
print (f'a ^ b = {e}')
print (f'~a = {f}')
print (f'Побитовый сдвиг a вправо на 2 знака = {g}')
print (f'Побитовый сдвиг a влево на 2 знака = {h}')
